
// shift and unshift function


let color = ['red','pink','blue','green','yellow']

color.unshift('pink')   //this function is add data in starting end

console.log(color)

let day = ['mon','tue','wed','thu','fri']

day.shift()      //this function is  remove data in starting end

console.log(day)    



// push and pop function


let animal =['dog','cat','mouse','tiger']


animal.push('horse')    //this function is add data in last

console.log(animal)


let fruit = ['apple','mango', 'banana']

fruit.pop()     //this function is remove data in last

console.log(fruit)

